public class Falls {
    private String data;
    private int height;
    public Falls() {

    }
    public Falls(String data, int height) {
        this.data = data;
        this.height = height;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
